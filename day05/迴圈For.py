# 迴圈 for
import random as r
# for loop 步進迴圈
for i in range(1, 5):  # 1, 2, 3, 4
    print(i, end=" ")
#-----------------------------------
print()  # 換行
for i in range(1, 5):
    print("Python", end=" ")
#-----------------------------------
print()  # 換行
for i in range(1, 5):
    print(i, "Python", end=" ")
#-----------------------------------
print()
for i in range(1, 10, 2):
    print(i, end=" ")
#-----------------------------------
print()
# for-loop 與數組的關係
scores = [100, 90, 80, 70, 60]
for score in scores:
    print(score, end=" ")


